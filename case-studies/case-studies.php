                        var $animatable = $(this);
                        if ($animatable.offset().top + $animatable.height() - 20 < offset) {
                            $animatable.removeClass("animatable").addClass("animated");
                        }
                    });
                };


                $(window).on("scroll", doAnimations);
                $(window).trigger("scroll");

